#include "libft.h"

t_list		*ft_lstnew(void const *content, size_t content_size)
{
	t_list		*list;
	size_t		csize;

	if (!(list = (t_list *)malloc(sizeof(t_list))))
		return (NULL);
	if (!content)
	{
		list->content = NULL;
		list->content_size = 0;
	}
	else
	{
		if (!(list->content = malloc(sizeof(content))))
			return (NULL);
		ft_memcpy(list->content, content, content_size);
		csize = content_size;
		list->content_size = csize;
	}
	list->next = NULL;
	return (list);
}
