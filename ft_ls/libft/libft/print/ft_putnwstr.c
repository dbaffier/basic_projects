#include "libft.h"

void	ft_putnwstr(wchar_t *str, size_t len)
{
	if (!str)
		write(1, "(null)", 6);
	while (*str && len > 0)
	{
		ft_putwchar(*str);
		len--;
		str++;
	}
}
