#include "ft_printf.h"

void	shuffle(t_flags *flags)
{
	if (F->LA || F->CP == 1)
		F->F0 = 0;
	if ((F->CW && !F->F0) || F->SG || F->SPC == 'p'
			|| (F->SPC != 'd' && F->SPC != 'i' && F->SPC != 'D'))
		F->SP = 0;
	if (F->SPC != 'd' && F->SPC != 'D' && F->SPC != 'i')
		F->SG = 0;
	if (F->SPC != 'x' && F->SPC != 'X' && F->SPC != 'o' && F->SPC != 'O')
		F->PR = 0;
	if (F->CP == 2 && F->SPC == 's')
		F->P = 0;
}
